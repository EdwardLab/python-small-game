from turtle import*
#turtle抬笔前设置
showturtle()
title("小屋")
penup()
pensize(20)
pencolor("green")
right(90)
fd(600)
left(90)
bk(700)
write("|   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |   |  ")
pendown()
#开始绘制
fd(1400)
penup()
#将笔往下绘制，绘制地形草坪
home()
bk(200)
pendown()
pencolor("black")
#使用循环绘制楼房
for i in range(2):
    fd(500)
    right(90)
    bk(500)
    right(90)
pendown()
left(90)
fd(500)
right(45)
fd(400)
right(95)
fd(390)
penup()
goto(100,100)
pendown()
circle(90)
penup()
goto(500,800)
pendown()
pencolor("red")
circle(100)
done()
turtle.position()
